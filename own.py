# =====================   FOR & WHILE LOOP   ======================
for i in range(5):
    print(i)

c = 0                 # from 0
while  c < 5:         # prints less than 5 digits
    print(c)
    c += 1                # will increment by 1 ( 0 1 2 3 4)

# ====================   / & // difference   =========================
